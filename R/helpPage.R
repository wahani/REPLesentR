helpPage <- function() {
  Slide(c(
    "",
    "HELP PAGE",
    "",
    "",
    "The following commands are available",
    "-   n -- next slide",
    "-   p -- previous slide",
    "-   f -- first slide",
    "-   l -- last slide",
    "-   e -- evaluate code",
    "-  ee -- evaluate code until here",
    "-  cc -- recompile slide deck",
    "-   q -- quit presentation",
    "-   h -- show this help",
    ""
  ))
}
